int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned cpt;

	cpt = 0;
	while ((s1[cpt] || s2[cpt]) && (cpt < n))
	{
		if (s1[cpt] < s2[cpt])
			return (-1);
		if (s1[cpt] > s2[cpt])
			return (0);
		cpt++;
	}
	return (0);
}
