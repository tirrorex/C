int		ft_num(char c)
{
	if ((c >= '0') && (c <= '9'))
	{
		return (1);
	}
	return (0);
}

int		ft_str_is_numeric(char *str)
{
	int cpt;

	cpt = 0;
	while (str[cpt] != '\0')
	{
		if (!ft_num(str[cpt]))
		{
			return (0);
		}
		cpt++;
	}
	return (1);
}
