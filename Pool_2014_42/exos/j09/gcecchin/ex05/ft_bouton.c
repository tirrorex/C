#include <stdlib.h>

int		ft_bouton(int i, int j, int k)
{
	if ((i >= k && i <= j) || (i >= j && i <= k))
		return (i);
	if ((j >= i && j <= k) || (j >= k && j <= i))
		return (j);
	if ((k >= j && k <= i) || (k >= i && k <= j))
		return (k);
	return (i);
}
