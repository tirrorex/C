int		ft_max(int *tab, int length)
{
	int i;
	int j;

	i = 0;
	j = 1;
	while ((i + j) < length)
	{
		if (tab[i] > tab[i + j])
			j = j + 1;
		else if (tab[i] <= tab[i + j])
			i = i + j;
	}
	return (tab[i]);
}
