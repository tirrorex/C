int ft_max(int *tab, int length)
{
	int nb;
	int i;

	nb = 0;
	i = 0;
	if (length > 0)
	{
		while (i < length)
		{
			if (tab[i] > nb)
				nb = tab[i];
			i++;
		}
		return (nb);
	}
	else
		return (0);
}
