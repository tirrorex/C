int	ft_iterative_factorial(int nb)
{
	int	r;

	r = 1;
	if (nb <= 0 || nb > 12)
		r = 0;
	while (nb > 0 && nb <= 12)
	{
		r = r * nb;
		nb = nb - 1;
	}
	return (r);
}
