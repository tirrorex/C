#include "libft.h"

void		ft_tabdel_array(char **tab, int i)
{
	while (tab[i + 1])
	{
		tab[i] = tab[i + 1];
		i++;
	}
	ft_strdel(&tab[i + 1]);
	tab[i] = 0;
}
