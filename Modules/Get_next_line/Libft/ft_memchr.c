#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*p;

	if (s)
	{
		p = (char*)s;
		while (n--)
		{
			if ((char)c != *p)
				p++;
			else
				return (p);
		}
	}
	return (NULL);
}
